package Model.AMReportPackage;

/**
 *
 * @author Principal
 */
public class AudioAmplifierInformation {
    private String audioAmplifierManuf;
    private String audioAmplifierModel;
    private String audioAmplifierSerialNum;

    /**
     * @return the audioAmplifierManuf
     */
    public String getAudioAmplifierManuf() {
        return audioAmplifierManuf;
    }

    /**
     * @param audioAmplifierManuf the audioAmplifierManuf to set
     */
    public void setAudioAmplifierManuf(String audioAmplifierManuf) {
        this.audioAmplifierManuf = audioAmplifierManuf;
    }

    /**
     * @return the audioAmplifierModel
     */
    public String getAudioAmplifierModel() {
        return audioAmplifierModel;
    }

    /**
     * @param audioAmplifierModel the audioAmplifierModel to set
     */
    public void setAudioAmplifierModel(String audioAmplifierModel) {
        this.audioAmplifierModel = audioAmplifierModel;
    }

    /**
     * @return the audioAmplifierSerialNum
     */
    public String getAudioAmplifierSerialNum() {
        return audioAmplifierSerialNum;
    }

    /**
     * @param audioAmplifierSerialNum the audioAmplifierSerialNum to set
     */
    public void setAudioAmplifierSerialNum(String audioAmplifierSerialNum) {
        this.audioAmplifierSerialNum = audioAmplifierSerialNum;
    }
}

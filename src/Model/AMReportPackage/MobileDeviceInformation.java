package Model.AMReportPackage;

/**
 *
 * @author Principal
 */
public class MobileDeviceInformation {
    private String mobileDeviceManuf;
    private String mobileDeviceModel;
    private String mobileDeviceSerialNum;

    /**
     * @return the mobileDeviceManuf
     */
    public String getMobileDeviceManuf() {
        return mobileDeviceManuf;
    }

    /**
     * @param mobileDeviceManuf the mobileDeviceManuf to set
     */
    public void setMobileDeviceManuf(String mobileDeviceManuf) {
        this.mobileDeviceManuf = mobileDeviceManuf;
    }

    /**
     * @return the mobileDeviceModel
     */
    public String getMobileDeviceModel() {
        return mobileDeviceModel;
    }

    /**
     * @param mobileDeviceModel the mobileDeviceModel to set
     */
    public void setMobileDeviceModel(String mobileDeviceModel) {
        this.mobileDeviceModel = mobileDeviceModel;
    }

    /**
     * @return the mobileDeviceSerialNum
     */
    public String getMobileDeviceSerialNum() {
        return mobileDeviceSerialNum;
    }

    /**
     * @param mobileDeviceSerialNum the mobileDeviceSerialNum to set
     */
    public void setMobileDeviceSerialNum(String mobileDeviceSerialNum) {
        this.mobileDeviceSerialNum = mobileDeviceSerialNum;
    }
}

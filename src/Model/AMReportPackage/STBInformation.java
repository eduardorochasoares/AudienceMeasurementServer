public class STBInformation {
    private String stbManuf;
    private String stbModel;
    private String stbSerialNum;

    /**
     * @return the stbManuf
     */
    public String getStbManuf() {
        return stbManuf;
    }

    /**
     * @param stbManuf the stbManuf to set
     */
    public void setStbManuf(String stbManuf) {
        this.stbManuf = stbManuf;
    }

    /**
     * @return the stbModel
     */
    public String getStbModel() {
        return stbModel;
    }

    /**
     * @param stbModel the stbModel to set
     */
    public void setStbModel(String stbModel) {
        this.stbModel = stbModel;
    }

    /**
     * @return the stbSerialNum
     */
    public String getStbSerialNum() {
        return stbSerialNum;
    }

    /**
     * @param stbSerialNum the stbSerialNum to set
     */
    public void setStbSerialNum(String stbSerialNum) {
        this.stbSerialNum = stbSerialNum;
    }

  
    
}
